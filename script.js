async function createGame() {
    myName = document.getElementById('player-name').value.trim() || '';
    if (!myName) { showToast('¡Falta el nombre! 😤'); return; }
    try { localStorage.setItem('sarasa_nickname', myName); } catch(e) {}

    isHost = true;
    const code   = generateRoomCode();
    myId         = 'HOST';
    const peerId = 'sarasa-' + code;

    const createBtn = document.querySelector('#step-action button:not(.btn-secondary):not(.btn-back)');
    if (createBtn) { createBtn.disabled = true; createBtn.innerText = 'Creando sala...'; }

    try {
        const res = await fetch('preguntas.json');
        if (res.ok) gameQuestions = await res.json();
    } catch (e) { console.warn('Usando preguntas de respaldo.'); }

    // Build the in-game deck: unseen prompts first, seen ones as fallback
    serverState.unusedPrompts = buildShuffledDeck(gameQuestions);
    peer = new Peer(peerId);

    peer.on('error', err => {
        console.error('Host peer error:', err);
        isHost = false;
        if (createBtn) { createBtn.disabled = false; createBtn.innerText = 'Crear Nueva Sala 🏠'; }
        showToast(err.type === 'unavailable-id'
            ? 'Ese código ya está en uso, intentá de nuevo.'
            : `Error al crear la sala: ${err.type}`);
        peer.destroy(); peer = null;
    });

    peer.on('open', () => {
        serverState.roomCode = code;
        serverState.players.push({ id: myId, name: myName, score: 0 });

        document.getElementById('host-controls').style.display    = 'block';
        document.getElementById('room-display-tag').innerText     = `SALA: ${code}`;
        document.getElementById('room-display-tag').style.display = 'block';
        document.getElementById('lobby-code-big').innerText       = code;

        if (createBtn) { createBtn.disabled = false; createBtn.innerText = 'Crear Nueva Sala 🏠'; }

        showScreen('screen-lobby');
        broadcast({ type: 'LOBBY_UPDATE', players: serverState.players, botIds: [], code, phase: serverState.phase });
        showToast(`¡Sala ${code} creada! Compartí el código 🏠`, 'accent', 3000);
    });

    peer.on('connection', connection => {
        hostConnections.push(connection);
        connection.on('data', data => handleCommandFromClient(data));
        connection.on('close', () => {
            hostConnections = hostConnections.filter(c => c !== connection);
            if (serverState.phase === 'LOBBY') {
                serverState.players = serverState.players.filter(p => p.id !== connection.peer);
                broadcast({
                    type: 'LOBBY_UPDATE',
                    players: [...serverState.players, ...serverState.bots],
                    code: serverState.roomCode,
                    phase: serverState.phase,
                    botIds: serverState.bots.map(b => b.id)
                });
            }
        });
    });
}

// ── Host broadcast & command handling ────────────────────────────────────────

function broadcast(data) {
    handleGameState(data);
    hostConnections.forEach(c => c.send(data));
}

function handleCommandFromClient(data) {

    if (data.type === 'CMD_JOIN') {
        // Prevent a real player from accidentally getting an ID that looks like a bot
        const idx = serverState.players.findIndex(p => p.id === data.id);
        if (idx !== -1) serverState.players[idx].name = data.name;
        else serverState.players.push({ id: data.id, name: data.name, score: 0, isBot: false });
        broadcast({
            type: 'LOBBY_UPDATE',
            players: [...serverState.players, ...serverState.bots],
            code: serverState.roomCode,
            phase: serverState.phase,
            botIds: serverState.bots.map(b => b.id)
        });
    }

    else if (data.type === 'CMD_SUBMIT_ANSWERS') {
        const pId = data.id;
        for (const [qId, text] of Object.entries(data.answers)) {
            if (!serverState.answers[qId]) serverState.answers[qId] = [];
            if (!serverState.answers[qId].some(a => a.authorId === pId)) {
                const isSafe = data.safeAnswerUsed && data.safeAnswerUsed[qId] ? true : false;
                const isEmpty = data.isFlush && (!text || text.trim() === '');
                const player = serverState.players.find(p => p.id === pId);
                serverState.answers[qId].push({
                    authorId: pId,
                    authorName: player ? player.name : 'Alguien',
                    text: isEmpty ? '' : text,
                    isSafe,
                    isEmpty,
                    isFlush: !!data.isFlush && !isEmpty  // timed-out but had something typed
                });
            }
        }

        // Broadcast PLAYER_READY so all clients update their tracker
        broadcast({ type: 'PLAYER_READY', playerId: pId });

        checkAllAnswersIn();
    }

    else if (data.type === 'CMD_RETRACT_ANSWERS') {
        const pId = data.id;
        for (const qId of Object.keys(serverState.answers)) {
            serverState.answers[qId] = serverState.answers[qId].filter(a => a.authorId !== pId);
        }
        // Note: No re-broadcast of ready status needed; client handles locally
    }

    else if (data.type === 'CMD_VOTE') {
        const currentPrompt = serverState.prompts[serverState.currentVoteIndex];
        if (!serverState.votes[currentPrompt.id]) serverState.votes[currentPrompt.id] = [];
        if (!serverState.votes[currentPrompt.id].some(v => v.voterId === data.voterId)) {
            serverState.votes[currentPrompt.id].push({ voterId: data.voterId, votedFor: data.authorId });
        }
        // Check if all eligible voters have voted
        const promptAnswers = serverState.answers[currentPrompt.id] || [];
        const eligibleVoters = serverState.currentRound <= 2
            ? serverState.players.filter(p => !promptAnswers.some(a => a.authorId === p.id))
            : serverState.players;
        if ((serverState.votes[currentPrompt.id] || []).length >= eligibleVoters.length) {
            clearTimeout(serverState.timerTimeout);
            processVotingResults();
        }
    }
}

// ── Game flow ─────────────────────────────────────────────────────────────────

function startGame() {
    const totalPlayers = serverState.players.length + serverState.bots.length;
    if (totalPlayers < 3) {
        showToast('Se necesitan al menos 3 jugadores 😅');
        return;
    }
    serverState.currentRound = 1;
    serverState.players.forEach(p => p.score = 0);
    serverState.bots.forEach(b => b.score = 0);
    // Merge bots into the players list for the game
    serverState.players = [...serverState.players, ...serverState.bots];
    serverState.bots = [];
    showToast('¡Empieza el juego! 🧉🔥', 'accent', 2500);
    startRound();
}

/**
 * Resolve special placeholders in a prompt for a given player list.
 * <ANYPLAYER> picks one random player name (same for everyone in the round).
 * The resolved name is stored in prompt.resolvedPlaceholders so clients all see the same name.
 */
function resolvePlaceholders(prompt, players) {
    if (!prompt.prompt.includes('<ANYPLAYER>')) return prompt;
    // Pick once per prompt (deterministic for this round)
    if (!prompt._resolvedAnyPlayer) {
        const pick = players[Math.floor(Math.random() * players.length)];
        prompt._resolvedAnyPlayer = pick ? pick.name : 'Alguien';
    }
    return {
        ...prompt,
        prompt: prompt.prompt.replace(/<ANYPLAYER>/g, prompt._resolvedAnyPlayer),
        _resolvedAnyPlayer: prompt._resolvedAnyPlayer
    };
}

function startRound() {
    serverState.phase            = 'ANSWERING';
    serverState.answers          = {};
    serverState.votes            = {};
    serverState.currentVoteIndex = 0;

    const players = serverState.players;
    const N       = players.length;

    if (serverState.currentRound <= 2) {
        if (serverState.unusedPrompts.length < N)
            serverState.unusedPrompts = buildShuffledDeck(gameQuestions);
        serverState.prompts = serverState.unusedPrompts.splice(0, N)
            .map(p => resolvePlaceholders(p, players));
        // Mark these prompts as seen across games
        historyMark(serverState.prompts);
        for (let i = 0; i < N; i++) {
            players[i].id && (serverState.assignments[players[i].id] = [
                serverState.prompts[i],
                serverState.prompts[(i + 1) % N]
            ]);
        }
    } else {
        if (serverState.unusedPrompts.length < 1)
            serverState.unusedPrompts = buildShuffledDeck(gameQuestions);
        const fp = resolvePlaceholders(serverState.unusedPrompts.splice(0, 1)[0], players);
        serverState.prompts = [fp];
        // Mark final-round prompt as seen
        historyMark([fp]);
        players.forEach(p => serverState.assignments[p.id] = [fp]);
    }

    broadcastAnsweringPhase();
}

function broadcastAnsweringPhase() {
    const players = serverState.players;

    hostConnections.forEach(c => c.send({
        type: 'PHASE_ANSWERING',
        round: serverState.currentRound,
        assignments: serverState.assignments[c.peer],
        players: players,
        time: 60
    }));

    handleGameState({
        type: 'PHASE_ANSWERING',
        round: serverState.currentRound,
        assignments: serverState.assignments[myId],
        players: players,
        time: 60
    });

    serverState.timerTimeout = setTimeout(() => {
        if (serverState.phase === 'ANSWERING') forceSubmitMissing();
    }, 60000);

    // Bots submit their answers after a short random delay (3-8s)
    const botDelay = 3000 + Math.random() * 5000;
    setTimeout(() => {
        if (serverState.phase === 'ANSWERING') submitBotAnswers();
    }, botDelay);
}

function forceSubmitMissing() {
    // Ask clients to flush their current textbox content before we finalize
    broadcast({ type: 'CMD_FORCE_FLUSH' });

    // Give a brief window for clients to send their in-progress answers
    setTimeout(() => {
        for (const [pId, assignedPrompts] of Object.entries(serverState.assignments)) {
            assignedPrompts.forEach(q => {
                if (!serverState.answers[q.id]) serverState.answers[q.id] = [];
                if (!serverState.answers[q.id].some(a => a.authorId === pId)) {
                    // No answer at all — mark as empty (handled in voting)
                    const player = serverState.players.find(p => p.id === pId);
                    serverState.answers[q.id].push({
                        authorId: pId,
                        authorName: player ? player.name : 'Alguien',
                        text: '',
                        isSafe: true,
                        isEmpty: true
                    });
                }
            });
        }
        startVotingPhase();
    }, 1500);
}

function startVotingPhase() {
    serverState.phase = 'VOTING';

    if (serverState.currentVoteIndex >= serverState.prompts.length) {
        serverState.currentRound++;
        if (serverState.currentRound > serverState.maxRounds) {
            broadcast({ type: 'PHASE_SCORES', players: serverState.players });
        } else {
            broadcast({
                type: 'ROUND_SCORES',
                round: serverState.currentRound - 1,
                players: serverState.players
            });
            setTimeout(() => startRound(), 5000);
        }
        return;
    }

    const currentPrompt = serverState.prompts[serverState.currentVoteIndex];
    let promptAnswers   = serverState.answers[currentPrompt.id] || [];

    if (serverState.currentRound <= 2 && promptAnswers.length < 2) {
        const safety = findReplacementAnswer(currentPrompt.id);
        promptAnswers.push({
            authorId: 'DUMMY',
            authorName: safety.authorName || 'El Sistema',
            text: safety.text + ' (Reciclada)'
        });
    }

    // Feature 3: if one answer is empty, the real one wins automatically with half-points
    const emptyIdx = promptAnswers.findIndex(a => a.isEmpty);
    const realIdx  = promptAnswers.findIndex(a => !a.isEmpty && a.authorId !== 'DUMMY');
    if (emptyIdx !== -1 && realIdx !== -1 && serverState.currentRound <= 2) {
        // Auto-resolve without voting — real answer gets half-points
        promptAnswers[realIdx] = { ...promptAnswers[realIdx], isSafe: true };
        // Store resolved answers so processVotingResults can read them
        serverState.answers[currentPrompt.id] = promptAnswers;
        broadcast({
            type: 'PHASE_VOTING_SKIP',
            prompt: currentPrompt,
            answers: promptAnswers,
            winnerId: promptAnswers[realIdx].authorId
        });
        setTimeout(() => processVotingResults(), 3000);
        return;
    }

    // Feature 4: if both answers are identical (Cicuta), resolve immediately without voting
    const nonDummy = promptAnswers.filter(a => a.authorId !== 'DUMMY');
    const isCicuta = serverState.currentRound <= 2
        && nonDummy.length === 2
        && nonDummy[0].text.trim().toLowerCase() === nonDummy[1].text.trim().toLowerCase()
        && nonDummy[0].text.trim() !== '';
    if (isCicuta) {
        serverState.answers[currentPrompt.id] = promptAnswers;
        broadcast({
            type: 'PHASE_VOTING_SKIP',
            prompt: currentPrompt,
            answers: promptAnswers,
            isCicuta: true
        });
        setTimeout(() => processVotingResults(), 3000);
        return;
    }

    promptAnswers = [...promptAnswers].sort(() => 0.5 - Math.random());

    broadcast({
        type: 'PHASE_VOTING',
        round: serverState.currentRound,
        prompt: currentPrompt,
        answers: promptAnswers,
        time: 20
    });

    // Bots vote after a short delay (1-4s), weighted toward what real players voted
    const botVoteDelay = 1000 + Math.random() * 3000;
    setTimeout(() => {
        if (serverState.phase === 'VOTING') {
            castBotVotes(currentPrompt, promptAnswers);
            // Check if all eligible voters have now voted
            const eligibleVoters = serverState.currentRound <= 2
                ? serverState.players.filter(p => !promptAnswers.some(a => a.authorId === p.id))
                : serverState.players;
            if ((serverState.votes[currentPrompt.id] || []).length >= eligibleVoters.length) {
                clearTimeout(serverState.timerTimeout);
                processVotingResults();
            }
        }
    }, botVoteDelay);

    serverState.timerTimeout = setTimeout(() => processVotingResults(), 20000);
}

function findReplacementAnswer(excludeQId) {
    const allHistory = [];
    Object.keys(serverState.answers).forEach(qId => {
        if (qId != excludeQId) allHistory.push(...serverState.answers[qId]);
    });
    return allHistory.length > 0
        ? allHistory[Math.floor(Math.random() * allHistory.length)]
        : { authorName: 'El Sistema', text: '¡Sarasa Cósmica!' };
}

function processVotingResults() {
    const currentPrompt = serverState.prompts[serverState.currentVoteIndex];
    const promptAnswers = serverState.answers[currentPrompt.id] || [];
    const votes         = serverState.votes[currentPrompt.id]   || [];

    // Feature 3: if one answer is empty, real answer wins with all votes (half-points via isSafe)
    const hasEmpty = promptAnswers.some(a => a.isEmpty);
    const realAnswer = promptAnswers.find(a => !a.isEmpty && a.authorId !== 'DUMMY');

    let isJinx = !hasEmpty && serverState.currentRound <= 2
        && promptAnswers.length === 2
        && promptAnswers.filter(a => !a.isEmpty).length === 2
        && promptAnswers[0].text.toLowerCase() === promptAnswers[1].text.toLowerCase();

    // For empty-vs-real: fabricate votes so real answer gets full eligible voter count
    let effectiveVotes = votes;
    if (hasEmpty && realAnswer) {
        const eligibleVoters = serverState.players.filter(p =>
            !promptAnswers.some(a => a.authorId === p.id)
        );
        effectiveVotes = eligibleVoters.map(p => ({ voterId: p.id, votedFor: realAnswer.authorId }));
    }

    const totalVotes       = effectiveVotes.length;
    const roundMultiplier  = serverState.currentRound === 2 ? 2 : serverState.currentRound === 3 ? 3 : 1;

    const resultsData = promptAnswers.map(ans => {
        // Skip empty answers from results display (they show up as a special card)
        const author     = serverState.players.find(p => p.id === ans.authorId);
        const authorName = ans.authorId === 'DUMMY' ? ans.authorName : (author ? author.name : 'Desconocido');
        const voterDetails = effectiveVotes
            .filter(v => v.votedFor === ans.authorId)
            .map(v => { const vi = serverState.players.find(p => p.id === v.voterId); return vi ? vi.name : 'Alguien'; });

        const voteCount = voterDetails.length;
        let pointsAdded = 0, quiplash = false;

        if (!isJinx && !ans.isEmpty && totalVotes > 0) {
            pointsAdded = Math.floor((voteCount / totalVotes) * 1000) * roundMultiplier;
            if (serverState.currentRound <= 2 && voteCount === totalVotes) {
                pointsAdded += 500 * roundMultiplier;
                quiplash = true;
            }
            // Safe answer (timed-out or explicit): half points
            if (ans.isSafe) pointsAdded = Math.floor(pointsAdded / 2);
        }

        if (author && !isJinx && !ans.isEmpty && ans.authorId !== 'DUMMY') author.score += pointsAdded;

        return { authorName, text: ans.text, isSafe: ans.isSafe || false, isEmpty: ans.isEmpty || false, _wasExplicitSafe: ans.isSafe && !ans.isFlush, votes: voteCount, voterNames: voterDetails, pointsAdded, quiplash };
    });

    broadcast({ type: 'VOTE_RESULT', prompt: currentPrompt, isJinx, hasEmpty, results: resultsData });

    setTimeout(() => {
        serverState.currentVoteIndex++;
        startVotingPhase();
    }, 8000);
}
